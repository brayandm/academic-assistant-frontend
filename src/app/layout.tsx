import "./globals.css";
import { Inter } from "next/font/google";
import { NextAuthProvider } from "@/providers/NextAuthProvider";
import { requiredRoles } from "@/lib/auth";
import Logout from "@/components/Logout";
import { ApolloClientProvider } from "@/providers/ApolloClientProvider";
import { GraphqlRequestClientProvider } from "@/providers/GraphqlRequestClientProvider";
import { getServerSession } from "next-auth";
import { authOptions } from "@/lib/auth";
import { MaterialUIProvider } from "@/providers/MaterialUIProvider";

const inter = Inter({ subsets: ["latin"] });

export const metadata = {
  title: "Academic Assistant",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  if (!(await requiredRoles([]))) return <Logout />;

  const session = await getServerSession(authOptions);

  return (
    <NextAuthProvider session={session!}>
      <ApolloClientProvider uri={process.env.GRAPHQL_URL_FROM_CLIENT!}>
        <GraphqlRequestClientProvider
          uri={process.env.GRAPHQL_URL_FROM_CLIENT!}
        >
          <MaterialUIProvider>
            <html lang="en">
              <head>
                <meta
                  name="viewport"
                  content="initial-scale=1, width=device-width"
                />
              </head>
              <body className={inter.className}>{children}</body>
            </html>
          </MaterialUIProvider>
        </GraphqlRequestClientProvider>
      </ApolloClientProvider>
    </NextAuthProvider>
  );
}
